<template>
    <div class="card">
        <div class="card-body">
            <h5 class="card-title">Wort</h5>
            <p class="card-text">{{ separateShuffledWord }}</p>
            <hr />
            <p v-bind:class="solutionCollapseClassAttribute"><strong>Lösung:</strong> {{ solution }}</p>
            <div class="d-flex justify-content-between">
            <button class="btn btn-outline-danger mr-auto" v-on:click="showSolution = !showSolution">Lösung</button>
            <slot></slot>
            </div>
        </div>
    </div>
</template>

<script>
    export default {
        name: "Word",
        props: ['shuffled', 'solution'],
        data() {
            return {
                showSolution: false
            }
        },
        computed: {
            separateShuffledWord: function () {
                return this.shuffled.split('').join(' ')
            },
            solutionCollapseClassAttribute: function () {
                return 'collapse' + (this.showSolution === true ? ' show' : '');
            }
        },
        watch: {
            shuffled: function(newVal, oldVal) {
                if (newVal !== oldVal) {
                    this.showSolution = false;
                }
            }
        }
    }
</script>

<style scoped>
    .card {
        min-width: 18rem;
    }
</style>